    <td>탑에어컨 개인보호정책
      <p>탑에어컨의 개인정보보호정책은 고객의 개인정보의 보호를 위해 개인 정보수집의 목적과 이용 및 정보의 정책적, 시스템적 
        보안에 관한 규정입니다. 이것은 개인의 사생활을 보호하고 불법적인 정보 유출을 철저히 막아 고객님께서 안심하고 쇼핑을 즐길 수 
        있도록 한것입니다. 탑에어컨의 고객 개인정보보호정책은 정보통신부에서 지정한 개인정보보호지침 제7조에 의거하여 작성되었으며, 
        개인보호정책은 새로운 서비스가 추가되거나 개선될 경우 수시로 내용이 변경될 수 있으므로 정기적으로 방문하셔서 확인해 보시기 바랍니다. 
        <br>
        탑에어컨는 항상 고객의 정보를 소중히 생각합니다. <br>
        <br>
        에듀메카(탑에어컨)의 개인정보보호정책은 아래와 같습니다</p>
