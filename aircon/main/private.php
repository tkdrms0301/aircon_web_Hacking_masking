        탑에어컨는 정통부 지정 개인정보보호지침(제22조1항)에 의거 관리책임자를 아래와 같이 지정합니다. </p>
      <p>▶직위 : 개인정보보호책임자 (CPO : Chief Privacy Officer) <br>
