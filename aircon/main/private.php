      <p>CPO(Chief Privacy Officer)는 탑에어컨회원 여러분(이하 &quot;귀하&quot;)의 개인정보를 
        보호하고, 유출을 방지하는 책임자로서, 귀하가 안심하고 탑에어컨를 이용할 수 있도록 도와드립니다.<br>
        * 개인정보보호에 대해 의견을 제시하거나 관련 문의사항이 있으면, 전화, e-mail로 연락하세요. </p>
      <p>개인정보 수집내용 및 수집방법 </p>
      <p>-탑에어컨는 다양한 부분에서 여러 가지 방법으로 귀하에 대한 개인정보를 수집하고 있습니다.<br>
        귀하의 주요 개인정보는 귀하가 탑에어컨에 회원등록을 할 때 수집됩니다. <br>
        회원가입시 수집되는 개인정보의 범위<br>
        -필수항목: 희망ID,비밀번호,성명,주민등록번호,전화번호,휴대폰번호,이메일(뉴스레터수신여부),관심분야<br>
        귀하가 탑에어컨에 자발적으로 제공하는 정보가 많을수록(또한 보다 정확할수록) 탑에어컨는 귀하의 경험에 맞추어 서비스를 
        제공할 수 있을 것입니다. 한번의 방문으로 귀하는 주어진 탑에어컨 ID로 탑에어컨의 수많은 서비스들을 즐기실 수 
        있습니다. </p>
      <p>개인정보 수집에 대한 동의</p>
      <p>-탑에어컨는 귀하께서 탑에어컨의 개인정보방침 또는 이용약관의 내용에 대해 동의함 버튼 또는 동의하지 않음 버튼을 
        클릭할 수 있는 절차를 마련하여, 동의함 버튼을 클릭하면 개인정보 수집에 대해 동의한 것으로 봅니다</p>
      <p>개인정보 수집이유 및 사용목적</p>
      <p>-사이트가 개인정보를 요구하는 이유 및 사용목적은 다음과 같으며, 해당 목적의 경우에만 사용됩니다.<br>
        탑에어컨가 귀하에게 맞춤화된 서비스를 제공하기 위함입니다. 즉, 커뮤니케이션 서비스 및 다양한 형태의 서비스들을 귀하에 
        대해 조금이라도 알고 있으면 귀하에게 보다 더 적절하고 유용한 광고와 내용들을 전달할 수 있으며, 이로 인해 귀하에게 보다 나은 
        서비스를 제공할 수 있게 됩니다.<br>
        탑에어컨에서 발생한 각종 이벤트 결과 당첨된 귀하께 경품을 배달하기 위한 자료로 사용하기 위함입니다.<br>
        탑에어컨로 귀하께서 요청하신 각종 질문사항 및 요구사항 대하여 수시로 답변을 드리기 위해 정보를 수집합니다.<br>
        탑에어컨는 서비스개선, 변경과 같은 공지사항을 알리거나, 협력요청사항을 보내기 위해 수집하며, 탑에어컨는 가끔씩 
        귀하가 흥미있어 할 서비스나 제품들에 관한 소식을 전자우편으로 보내드릴 것입니다.<br>
        가끔 탑에어컨에서 진행하는 이벤트나 기타 행사에서도 귀하의 점수나, 참가여부, 및 경품 추첨 상황 등에 대하여 최신 정보를 
        제공하기 위하여 귀하에게 전자우편주소를 요청할 때가 있습니다.<br>
        이러한 유형의 행사에 참여하시는 경우, 전자우편을 받지 않겠다는 옵션을 선택하지 않는한 자동적으로, 행사관련 메일을 받아 보시게 
        될 것입니다. </p>
      <p>개인정보 보유 및 이용기간 </p>
      <p>-일반적으로 가입 탈퇴시 또는 탑에어컨의 서비스 중단시 까지 보유ㆍ이용하게 됩니다.<br>
        탑에어컨는 귀하께 보내는 답변완료시 까지 이용할 것이며, 이 경우에는 데이터베이스 형태로 관리, 이용하지 않을 것입니다 
      </p>
      <p>개인정보를 제3자에게 제공하는 경우 <br>
        <br>
        -귀하의 개인정보는 정보통신부에서 지정한 개인정보보호지침 제3자 제공ㆍ활용시 본인의 동의를 얻어야 하는 정보입니다. 탑에어컨는 
        법률에서 정하는 경우를 제외하고는 귀하의 동의없이 제3자에게 제공하지 않습니다.<br>
        탑에어컨로 광고를 올리는 광고주나 링크가 되어 있는 웹사이트들이 귀하에 대하여 개인적으로 식별할 수 있는 정보를 수집할 
        수도 있다는 점을 반드시 명심하시기 바랍니다. 탑에어컨에 링크되어 있는 웹사이트들이 귀하의 개인 정보를 수집하는 행위에 
        대해서는 본 '개인정보보호정책'이 적용되지 않음도 아울러 알려 드립니다 </p>
      <p>개인정보의 열람,수정 및 탈퇴 </p>
      <p>-탑에어컨의 회원으로서 귀하의 개인정보를 열람하거나 수정하고자 할 경우에는 ID와 비밀번호를 사용하여 탑에어컨의 
        회원정보메뉴에서 개인정보 열람, 회원정보수정, 비밀번호 변경이 가능합니다.<br>
        탑에어컨회원에서 탈퇴하고자 할 경우, 로그인 후 홈페이지 첫 화면의 개인정보변경을 클릭하여 회원탈퇴를 하시거나 사이트 
        하단에 표시된 이메일 또는 개인정보보호책임자(CPO)에게 E-mail(※ 첨부 : 가입당시 사이트 URL, 이름, 아이디, 비번, 
        주민번호 뒷자리)을 보내시면 곧 조치해 드립니다. </p>
      <p>어린이의 프라이버시 보호 </p>
      <p>-탑에어컨는 온라인 환경에서 만14세 미만 어린이의 개인정보를 보호하는 것 역시 중요한 일로 생각하고 있습니다. 탑에어컨는 
        특별히 어린이가 유의하여야 할 정보를 게시하거나 제공하지는 않습니다. 그러나 만14세 미만의 어린이들은 탑에어컨에 개인정보를 
        보내기 전에 항상 부모님의 동의를 받아야 합니다. 또한 부모는 어린이의 탑에어컨사용을 금지하거나, 정보의 사용 활동에 
        개입할 수 있습니다.<br>
        탑에어컨는 만14세 미만의 이용자에 관한 정보를 제3자와 공유하지 않으며 이용자에 관한 정보를 매매하거나 대여하지 않습니다. 
        아울러 탑에어컨는 만14세 미만으로 표시된 이용자에게는 전자우편 형식의 광고나, 제안서를 보내지 않습니다.<br>
        탑에어컨는 부모의 사전 동의 없이, 사용자 확인이 가능한 정보를 공개 게시하거나 배포하지 않습니다.<br>
        탑에어컨는 해당 작업을 처리하는 데 필요한 정보 외에, 특정 게임, 경품 및 다른 활동을 미끼로 더 많은 정보를 공개하도록 
        유도하지 않습니다 . </p>
